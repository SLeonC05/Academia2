    <h3>Misión</h3>
    <p>La academia de idiomas Lesan Academy, es una unidad adscrita a la Vicerrectoría Académica, la cual desarrolla procesos de enseñanza y aprendizaje de lenguas extranjeras y promueve espacios culturales y académicos que sensibilizan a la comunidad universitaria frente a la diversidad y la identidad cultural, tanto en el contexto local como internacional.</p>

    <h3>Visión</h3>
    <p> La academia de idiomas Lesan Academy, a 2022 será una unidad académica líder a nivel local y nacional que a través del aprendizaje y enseñanza de las lenguas extranjeras potenciará la movilidad académica, la interacción cultural y el desarrollo de procesos académicos e investigativos requeridos por los profesionales del mundo de hoy.</p>

    <h3>Objetivos</h3>
    <p>Desarrollar en los estudiantes las competencias comunicativas en lengua extranjera que faciliten las prácticas de comprensión, expresión oral, interacción, interpretación de textos o discursos y los intercambios culturales relacionados con los saberes académicos específicos de los estudiantes.</p>

